#Accessing Characters
R = "Tpoint Tech"

print("Data Type:",R)

print("R[0]:", R[0])
print("R[8]:",R[8])

#Accessing String with Negative Indexing

print("R[-3]:",R[-3])
print("R[-7]:",R[-7])

#String Slicing
print("R[4:]", R[4:])
print("R[8:]:",R[8:])
print("R[:5]:",R[:5])
print("R[:9]:",R[:9])
print("R[:-1]",R[:-1])
print("R[::-1]",R[::-1])

print("R[2::6]",R[2::6])

